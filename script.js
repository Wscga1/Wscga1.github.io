function setLanguage(language) {
  const dictionary = translations[language];

  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.title = language === "zh" ? "陈国傲 | AI 产品经理" : "Guoao Chen | AI Product Manager";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = dictionary[element.dataset.i18n];
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    element.setAttribute("aria-label", dictionary[element.dataset.i18nAria]);
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    element.alt = dictionary[element.dataset.i18nAlt];
  });

  document.querySelectorAll(".language-button").forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  try {
    localStorage.setItem("preferred-language", language);
  } catch {
    // Language selection remains available when browser storage is restricted.
  }
}

let savedLanguage = "zh";

try {
  savedLanguage = localStorage.getItem("preferred-language") || "zh";
} catch {
  // Chinese remains the default when browser storage is restricted.
}

setLanguage(savedLanguage === "en" ? "en" : "zh");

document.querySelectorAll(".language-button").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.language));
});
